"use server";

/**
 * @fileOverview A document summarization AI agent.
 *
 * - summarizeDocument - A function that handles the document summarization process.
 * - SummarizeDocumentInput - The input type for the summarizeDocument function.
 * - SummarizeDocumentOutput - The return type for the summarizeDocument function.
 */

import { ai } from "@/ai/genkit";
import { z } from "genkit";
import { saveSummary } from "@/app/actions/documents";

// INPUT
const SummarizeDocumentInputSchema = z.object({
  documentId: z.string().describe("The ID of the document."),
  documentText: z
    .string()
    .describe("The text content of the document to summarize."),
  detail: z
    .boolean()
    .optional()
    .default(false)
    .describe("Whether to generate a detailed summary."),
});
export type SummarizeDocumentInput = z.infer<
  typeof SummarizeDocumentInputSchema
>;

// OUTPUT
const SummarizeDocumentOutputSchema = z.object({
  summary: z.string().describe("The summary of the document."),
});
export type SummarizeDocumentOutput = z.infer<
  typeof SummarizeDocumentOutputSchema
>;

export async function summarizeDocument(
  input: SummarizeDocumentInput
): Promise<SummarizeDocumentOutput> {
  return summarizeDocumentFlow(input);
}

const prompt = ai.definePrompt({
  name: "summarizeDocumentPrompt",
  input: { schema: SummarizeDocumentInputSchema },
  output: { schema: SummarizeDocumentOutputSchema },
  prompt: `Sen uzman bir özetleme asistanısın.  
  Görevin, verilen dokümanı açık, anlaşılır ve bilgilendirici şekilde özetlemektir.

  Eğer kullanıcı detaylı özet istemişse, daha kapsamlı bir özet oluştur.

  Doküman Metni:
  {{{documentText}}}

  Detaylı Özet İstendi mi?
  {{#if detail}}Evet{{else}}Hayır{{/if}}

  Lütfen tüm yanıtları TÜRKÇE olarak üret.`,
});

// Flow: Üret → DB’ye Kaydet → Döndür
const summarizeDocumentFlow = ai.defineFlow(
  {
    name: "summarizeDocumentFlow",
    inputSchema: SummarizeDocumentInputSchema,
    outputSchema: SummarizeDocumentOutputSchema,
  },
  async (input) => {
    const { output } = await prompt(input);
    return output!;
  }
);
